"""
Модуль для работы с кулинарной книгой и объединения текстовых файлов.

Содержит функции:
- read_cook_book(file_path: str) -> dict
- get_shop_list_by_dishes(dishes: list[str], person_count: int, cook_book: dict) -> dict
- merge_files(file_paths: list[str], output_path: str) -> None
"""


def read_cook_book(file_path):  # Определяем функцию для чтения файла рецептов
    """
    Читает файл рецептов и возвращает словарь cook_book.

    Формат файла:
    Название блюда
    количество ингредиентов (число)
    Ингредиент | количество | единица измерения
    ...
    (пустая строка — разделитель рецептов)

    :param file_path: путь к файлу с рецептами
    :return: словарь вида {название_блюда: [список ингредиентов]}
    """
    cook_book = {}  # Инициализируем пустой словарь для хранения рецептов
    with open(file_path, 'r', encoding='utf-8') as file:  # Открываем файл на чтение в UTF-8
        lines = file.readlines()  # Считываем все строки файла в список

    i = 0  # Индекс для прохода по строкам
    while i < len(lines):  # Цикл по всем строкам файла
        # Пропускаем пустые строки
        if not lines[i].strip():  # Если строка состоит только из пробельных символов
            i += 1  # Переходим к следующей строке
            continue  # Пропускаем остаток тела цикла

        # Название блюда
        dish_name = lines[i].strip()  # Убираем лишние пробелы и переводы строк
        i += 1  # Перемещаемся к следующей строке

        # Читаем количество ингредиентов
        if i >= len(lines):  # Проверяем, не вышли ли за пределы списка строк
            break  # Прерываем цикл, если строк больше нет
        ingredients_count = int(lines[i].strip())  # Преобразуем строку в целое число
        i += 1  # Переходим к первой строке с ингредиентом

        ingredients = []  # Список для хранения ингредиентов текущего блюда
        for _ in range(ingredients_count):  # Цикл по количеству ингредиентов
            if i >= len(lines):  # Если строки кончились раньше времени
                break  # Выходим из цикла
            line = lines[i].strip()  # Очищаем строку от пробелов и переводов
            if line:  # Если строка не пустая
                # Разбор строки ингредиента: имя | количество | мера
                parts = [p.strip() for p in line.split('|')]  # Делим по '|' и чистим пробелы
                if len(parts) == 3:  # Убеждаемся, что получилось ровно три части
                    ingredient = {  # Создаём словарь ингредиента
                        'ingredient_name': parts[0],  # Название ингредиента
                        'quantity': int(parts[1]),  # Количество (целое число)
                        'measure': parts[2]  # Единица измерения
                    }
                    ingredients.append(ingredient)  # Добавляем ингредиент в список блюда
            i += 1  # Переходим к следующей строке

        if ingredients:  # Если список ингредиентов не пуст
            cook_book[dish_name] = ingredients  # Сохраняем блюдо в словарь

    return cook_book  # Возвращаем заполненный словарь рецептов


def get_shop_list_by_dishes(dishes, person_count, cook_book):  # Функция для составления списка покупок
    """
    Возвращает список покупок для приготовления указанных блюд на заданное число персон.

    :param dishes: список названий блюд из cook_book
    :param person_count: количество персон
    :param cook_book: словарь с рецептами (результат read_cook_book)
    :return: словарь, где ключ — название ингредиента,
             значение — {'measure': str, 'quantity': int}
    """
    shop_list = {}  # Инициализируем пустой словарь для итогового списка покупок

    for dish in dishes:  # Перебираем все запрошенные блюда
        if dish not in cook_book:  # Если блюда нет в книге рецептов
            print(f"Внимание: блюдо '{dish}' отсутствует в книге рецептов, пропускаем.")  # Выводим предупреждение
            continue  # Переходим к следующему блюду
        for ingredient in cook_book[dish]:  # Перебираем ингредиенты текущего блюда
            name = ingredient['ingredient_name']  # Извлекаем название ингредиента
            measure = ingredient['measure']  # Извлекаем единицу измерения
            # Количество на одну персону * количество персон
            quantity = ingredient['quantity'] * person_count  # Вычисляем общее количество

            if name in shop_list:  # Если ингредиент уже есть в итоговом словаре
                shop_list[name]['quantity'] += quantity  # Добавляем количество к существующему
            else:  # Если ингредиент встречается впервые
                shop_list[name] = {  # Создаём новую запись
                    'measure': measure,  # Единица измерения
                    'quantity': quantity  # Количество
                }

    return shop_list  # Возвращаем составленный список покупок


def merge_files(file_paths, output_path):  # Функция для объединения файлов с сортировкой по числу строк
    """
    Объединяет несколько текстовых файлов в один, сортируя их по количеству строк.

    Результирующий файл имеет формат:
    имя файла 1
    число строк 1
    содержимое файла 1
    имя файла 2
    число строк 2
    содержимое файла 2
    ...

    :param file_paths: список путей к исходным файлам
    :param output_path: путь к итоговому файлу
    """
    # Собираем данные: (имя файла, количество строк, содержимое)
    files_data = []  # Список для хранения информации о каждом файле
    for path in file_paths:  # Перебираем все переданные пути к файлам
        with open(path, 'r', encoding='utf-8') as f:  # Открываем текущий файл на чтение
            lines = f.readlines()  # Считываем все строки
            # Количество строк — это длина списка lines
            # Если файл заканчивается пустой строкой, она тоже считается строкой (как в примере)
            line_count = len(lines)  # Подсчитываем количество строк
            content = ''.join(lines)  # Объединяем строки в единый текст (сохраняем переводы строк)
            if content and not content.endswith('\n'): # проверяет, что строка непустая и не заканчивается символом переноса строки.
                content += '\n' #если условие верно, добавляет \n в конец содержимого.
            files_data.append({  # Добавляем словарь с данными файла в общий список
                'name': path,  # Имя (путь) файла
                'line_count': line_count,  # Количество строк
                'content': content  # Содержимое файла
            })

    # Сортировка по количеству строк (возрастание)
    files_data.sort(key=lambda x: x['line_count'])  # Сортируем список по ключу 'line_count'

    # Запись результата
    with open(output_path, 'w', encoding='utf-8') as out_file:  # Открываем результирующий файл на запись
        for file_info in files_data:  # Перебираем отсортированные данные файлов
            out_file.write(f"{file_info['name']}\n")  # Записываем имя файла и перевод строки
            out_file.write(f"{file_info['line_count']}\n")  # Записываем количество строк и перевод
            out_file.write(file_info['content'])  # Записываем содержимое файла (переводы строк уже есть)


def main():  # Основная функция для демонстрации работы всех функций модуля
    """Пример использования всех функций."""
    # Задача 1: чтение кулинарной книги
    cook_book = read_cook_book('recipes.txt')  # Читаем рецепты из файла

    print("cook_book = {")  # Выводим начало словаря для наглядности
    for dish, ingredients in cook_book.items():  # Перебираем все блюда из кулинарной книги
        print(f"  '{dish}': [")  # Печатаем название блюда
        for ing in ingredients:  # Перебираем ингредиенты блюда
            print(f"    {ing},")  # Печатаем каждый ингредиент с отступом
        print("  ],")  # Закрываем список ингредиентов для текущего блюда
    print("}")  # Закрываем словарь

    # Задача 2: список покупок
    shop_list = get_shop_list_by_dishes(['Запеченный картофель', 'Омлет'], 2, cook_book)  # Составляем список покупок
    print("\nСписок покупок:")  # Выводим заголовок
    print(shop_list)  # Печатаем полученный словарь покупок

    # Задача 3: объединение файлов (пример со списком имён)
    # Для демонстрации создадим несколько тестовых файлов, если их нет.
    # Обычно предполагается, что файлы уже есть в папке.
    test_files = ['1.txt', '2.txt']  # Определяем список файлов для объединения
    merge_files(test_files, 'result.txt')  # Вызываем функцию объединения
    print("\nФайлы объединены в result.txt")  # Сообщаем об успешном завершении


if __name__ == '__main__':  # Проверяем, запущен ли скрипт как главный
    main()  # Запускаем основную функцию